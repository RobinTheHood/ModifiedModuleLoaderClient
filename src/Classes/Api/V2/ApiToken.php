namespace RobinTheHood\ModifiedModuleLoaderClient\Api\V2;

class ApiToken
{
    /** @var string */
    private $token;

    public function __construct(string $token)
    {
        $this->token = $token;
    }

    public function getToken(): string
    {
        return $this->token;
    }

    public function createBearer()
    {
        return 'Bearer ' . $this->token;
    }
}
